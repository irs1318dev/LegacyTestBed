package org.usfirst.frc1318.legoarmbot.shared;

public class DeltaConfiguration extends Configuration {

	public DeltaConfiguration(double theta1, double theta2, double length1, double length2) {
		this.theta1 = theta1;
		this.theta2 = theta2;
		this.length1 = length1;
		this.length2 = length2;
	}

}